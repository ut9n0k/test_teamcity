def amount(a: int, b: int):
    return a + b


def multiply(a: int, b: int):
    return a * b


def divide(a: int, b: int):
    return a / b


def subtract(a: int, b: int):
    return a - b


if __name__ == "__main__":
    print(amount(10, 2))
    print(multiply(10, 2))
    print(divide(10, 2))
    print(subtract(10, 2))
